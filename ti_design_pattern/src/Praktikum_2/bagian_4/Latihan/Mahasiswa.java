package Praktikum_2.bagian_4.Latihan;

public class Mahasiswa {
    // atribut private
    private String nama;
    private String nim;

    // setter nama
    public void setNama(String nama) {
        this.nama = nama;
    }

    // getter nama
    public String getNama() {
        return nama;
    }

    // setter nim
    public void setNim(String nim) {
        this.nim = nim;
    }

    // getter nim
    public String getNim() {
        return nim;
    }
}